//-------------------------------------------------------------------------------------------------------------------
//  @brief      MT9V032摄像头初始化
//  @param      NULL
//  @return     void					
//  @since      v1.0
//  Sample usage:		
//-------------------------------------------------------------------------------------------------------------------
void camera_init(void)
{
    //设置参数    具体请参看使用手册
    uint16 temp, i;
    uint8  send_buffer[4];
    
    uart_init (MT9V032_COF_UART, 9600);	//初始换串口 配置摄像头    
    uart_rx_irq_en(MT9V032_COF_UART);   //配置串口中断
    EnableInterrupts;                   //开启中断等待51返回数据
    
    //等待摄像头上电初始化成功
    systick_delay_ms(1000);
    uart_receive_flag = 0;
	
	EXP_Time=flash_read(252,0,uint16);
	if(EXP_Time==0xffff)
	{
		while(FLASH_EraseSector(252));	//擦除扇区
		systick_delay_ms(10);
		EXP_Time=EXP_Time_Initial;
		FLASH_WriteSector(252,(const uint8 *)&EXP_Time,16,0);//确认输入flash	253 254扇区用于电感 253扇区用于摄像头曝光
	}

     MT9V032_CFG[1][1]=EXP_Time;//EXP_Time;//调节曝光
	 
    //开始配置摄像头并重新初始化
    for(i=0; i<CONFIG_FINISH; i++)
    {
        send_buffer[0] = 0xA5;
        send_buffer[1] = MT9V032_CFG[i][0];
        temp = MT9V032_CFG[i][1];
        send_buffer[2] = temp>>8;
        send_buffer[3] = (uint8)temp;
        
        uart_putbuff(MT9V032_COF_UART,send_buffer,4);
        systick_delay_ms(2);
    }
    //等待摄像头初始化成功
    while(!uart_receive_flag);
    uart_receive_flag = 0;
    while((0xff != receive[1]) || (0xff != receive[2]));
    //以上部分对摄像头配置的数据全部都会保存在摄像头上51单片机的eeprom中
    //利用set_exposure_time函数单独配置的曝光数据不存储在eeprom中
    
    
    //获取配置便于查看配置是否正确
    get_config();
    
    
    //摄像头采集初始化
    DisableInterrupts;          //关闭所有中断配置DMA0和PORTA
    //DMA通道0初始化，PTA27触发源(默认上升沿)，源地址为B_IN_DATA(0)(PTB1-PTB8)，目的地址为：image，每次传输1Byte 传输完毕保持目的地址
    
    dma_portx2buff_init(DMA_CH0, (void *)&B_IN_DATA(0), (void *)image, A29, DMA_BYTE1, COL*ROW, DADDR_KEEPON);
    port_init(A29, ALT1 | DMA_RISING | PULLDOWN);  			//PCLK  触发源设置
    DMA_DIS(DMA_CH0);                                     		//禁用DMA通道
    DMA_IRQ_CLEAN(DMA_CH0);                               		//清除通道传输中断标志位
    DMA_IRQ_EN(DMA_CH0);                                  		//允许DMA通道中断
    DMA_EN(DMA_CH0);  											//使能DMA
    
    disable_irq(PORTB_IRQn);                             		//关闭PTC的中断
    //port_init(C7, ALT1 | IRQ_FALLING | PULLDOWN);      			//行中断
    port_init(B17, ALT1 | IRQ_FALLING | PULLDOWN);              //场中断，下降沿触发中断、下拉
    enable_irq (PORTB_IRQn);
    
}


//-------------------------------------------------------------------------------------------------------------------
//  @brief      MT9V032摄像头场中断
//  @param      NULL
//  @return     void			
//  @since      v1.0
//  Sample usage:				在isr.c里面先创建对应的中断函数，然后调用该函数(之后别忘记清除中断标志位)
//-------------------------------------------------------------------------------------------------------------------
void VSYNC(void)
{
    if(MT9V032_img_flag == IMG_START)                   //需要开始采集图像
    {
		disable_irq(PORTB_IRQn);
		MT9V032_img_flag = IMG_GATHER;                  //标记图像采集中
		dma_repeat(DMA_CH0,(void *)&B_IN_DATA(0),(void *)image[0],COL*ROW);
		
	}
	else                                        //图像采集错误
	{
		disable_irq(PORTB_IRQn);                        //关闭PTA的中断
		MT9V032_img_flag = IMG_FAIL;                    //标记图像采集失败
	}
}


